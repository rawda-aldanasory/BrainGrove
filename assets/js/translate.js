const languageselector = document.querySelector('select');

languageselector.addEventListener('change', (event) => {
    const selectedLang = event.target.value || 'en'; 
    setlangauge(selectedLang);
    localStorage.setItem('lang', selectedLang);
});

document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('lang') || 'en'; 
    setlangauge(savedLang);
    languageselector.value = savedLang; 
});

const setlangauge = (langauge) => {
    const elements = document.querySelectorAll('[data-tra]');
    elements.forEach((element) => {
        const key = element.getAttribute('data-tra');
        if (translation[langauge] && translation[langauge][key]) {
            element.innerHTML = translation[langauge][key];
        } else {
            console.warn(`No translation found for key: ${key}`);
        }
    });
    document.dir = langauge === 'ar' ? 'rtl' : 'ltr'; 
};

document.body.style.visibility = "hidden";
document.addEventListener("DOMContentLoaded", () => {
    document.body.style.visibility = "visible";
});
